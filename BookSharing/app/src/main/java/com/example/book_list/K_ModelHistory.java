package com.example.book_list;

public class K_ModelHistory {

    private int bookicon;
    private String BookName;
    private String PublisherName;
    private String AuthorName;
    private String donerName;

    public int getBookicon() {
        return bookicon;
    }

    public void setBookicon(int bookicon) {
        this.bookicon = bookicon;
    }

    public String getBookName() {
        return BookName;
    }

    public void setBookName(String bookName) {
        BookName = bookName;
    }

    public String getPublisherName() {
        return PublisherName;
    }

    public void setPublisherName(String publisherName) {
        PublisherName = publisherName;
    }

    public String getAuthorName() {
        return AuthorName;
    }

    public void setAuthorName(String authorName) {
        AuthorName = authorName;
    }

    public String getDonerName() {
        return donerName;
    }

    public void setDonerName(String donerName) {
        this.donerName = donerName;
    }

}
